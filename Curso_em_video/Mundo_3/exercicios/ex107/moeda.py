def aumentar(aumento, valor):
    valor_aumento = valor * (aumento / 100)
    return valor + valor_aumento


def diminuir(aumento, valor):
    valor_diminuição = valor * (aumento / 100)
    return valor - valor_diminuição


def dobro(valor):
    novo_valor = valor * 2
    return novo_valor


def metade(valor):
    novo_valor = valor / 2
    return novo_valor

